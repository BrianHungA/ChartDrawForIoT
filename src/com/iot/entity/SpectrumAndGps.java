package com.iot.entity;

public class SpectrumAndGps {

	private String time;
	private String timestamp;
	private String abs_X;
	private String abs_Y;
	private String abs_Z;
	private String abs_XYZ;
	private String gps_x;
	private String gps_y;
	private float speed;
	private float level;
	
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getAbs_X() {
		return abs_X;
	}
	public void setAbs_X(String abs_X) {
		this.abs_X = abs_X;
	}
	public String getAbs_Y() {
		return abs_Y;
	}
	public void setAbs_Y(String abs_Y) {
		this.abs_Y = abs_Y;
	}
	public String getAbs_Z() {
		return abs_Z;
	}
	public void setAbs_Z(String abs_Z) {
		this.abs_Z = abs_Z;
	}
	public String getAbs_XYZ() {
		return abs_XYZ;
	}
	public void setAbs_XYZ(String abs_XYZ) {
		this.abs_XYZ = abs_XYZ;
	}
	public String getGps_x() {
		return gps_x;
	}
	public void setGps_x(String gps_x) {
		this.gps_x = gps_x;
	}
	public String getGps_y() {
		return gps_y;
	}
	public void setGps_y(String gps_y) {
		this.gps_y = gps_y;
	}
	public float getSpeed() {
		return speed;
	}
	public void setSpeed(float speed) {
		this.speed = speed;
	}
	public float getLevel() {
		return level;
	}
	public void setLevel(float level) {
		this.level = level;
	}
	
}
